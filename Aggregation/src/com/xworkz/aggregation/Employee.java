package com.xworkz.aggregation;

public class Employee {
	
	String employeeName;
	String responsibleIn;
	int salary;
	double shiftTiming;
	int idNumber;
	
	public Employee(String employeeName, String responsibleIn, int salary, double shiftTiming, int idNumber) {
		this.employeeName=employeeName;
		this.responsibleIn=responsibleIn;
		this.salary=salary;
		this.shiftTiming=shiftTiming;
		this.idNumber=idNumber;
		
	}
	
	
	

}
