package com.xworkz.aggregation;

public class Patient {
	
	String patientName;
	int patientAge;
	String diease;
	char gender;
	String address;
	
	public Patient(String patientName, int patientAge, String diease, char gender, String address) {
		this.patientName=patientName;
		this.patientAge=patientAge;
		this.diease=diease;
		this.gender=gender;
		this.address=address;
	}

}
